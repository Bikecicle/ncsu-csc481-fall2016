package part1;

public class Box {
	
	public int posx;
	public int posy;
	public int width;
	public int height;
	public int[] color = new int[3];

	public Box(int posx, int posy, int width, int height, int[] color) {
		this.posx = posx;
		this.posy = posy;
		this.width = width;
		this.height = height;
		this.color = color;;
	}

}
